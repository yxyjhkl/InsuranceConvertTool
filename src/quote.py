def get_daily_quote(user_id: str = '') -> str:
    today = datetime.now().date()
    date_str = today.strftime('%Y%m%d')
    seed_str = f"{date_str}_{user_id}"
    seed = int(hashlib.md5(seed_str.encode()).hexdigest(), 16)
    random.seed(seed)
    quote = random.choice(INSURANCE_QUOTES)
    random.seed()
    return quote


def get_random_quote() -> str:
    return random.choice(INSURANCE_QUOTES)


def get_quote_with_date(user_id: str = '') -> tuple:
    quote = get_daily_quote(user_id)
    today = datetime.now().strftime('%Y年%m月%d日')
    return quote, today